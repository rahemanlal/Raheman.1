import tkinter as tk


class calculator:
    def __init__(self):
        self.window =tk.Tk()
        self.window.geometry("375x667")
        self.window.resizable(0_0)
        self.window.title("calculator")
        

def run(self):
    self.window.mainloop()

if __name__ == "__main__":
    calc = calculator
    calc =run()